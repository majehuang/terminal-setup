if [[ "$DRY_RUN" == "1" ]]; then
  log "Would replace managed block in $ZSHRC"
else
  python3 - "$ZSHRC" "$ZSH_BLOCK" <<'PY'
import re
import sys
from pathlib import Path

path = Path(sys.argv[1])
block = sys.argv[2]
text = path.read_text() if path.exists() else ""
pattern = r"\n?# >>> ghostty-starship-catppuccin setup >>>.*?# <<< ghostty-starship-catppuccin setup <<<\n?"
text = re.sub(pattern, "\n", text, flags=re.S).rstrip() + "\n\n" + block + "\n"
path.write_text(text)
PY
fi

