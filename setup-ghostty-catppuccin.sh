write_file "$GHOSTTY_CONFIG" "$GHOSTTY_CONTENT"

# 兼容旧路径：如果用户曾用过 macOS Application Support 路径，则给出提示但不覆盖
OLD_GHOSTTY_DIR="$HOME/Library/Application Support/com.mitchellh.ghostty"
if [[ -d "$OLD_GHOSTTY_DIR" ]]; then
  warn "检测到旧配置目录：$OLD_GHOSTTY_DIR"
  warn "本脚本使用 XDG 路径：$GHOSTTY_CONFIG"
fi

# 4. 写入 Starship Catppuccin Mocha 配置
STARSHIP_CONFIG="$HOME/.config/starship.toml"
run mkdir -p "$HOME/.config"
backup_file "$STARSHIP_CONFIG"

