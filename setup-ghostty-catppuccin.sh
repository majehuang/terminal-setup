#!/usr/bin/env bash
set -euo pipefail

# Ghostty + Starship + Catppuccin Mocha 一键配置脚本
# 目标平台：macOS + zsh
# 用法：bash setup-ghostty-catppuccin.sh
# 预览：bash setup-ghostty-catppuccin.sh --dry-run

DRY_RUN=0
OPEN_GHOSTTY=1
TS="$(date +%Y%m%d-%H%M%S)"

for arg in "$@"; do
  case "$arg" in
    --dry-run) DRY_RUN=1 ;;
    --no-open) OPEN_GHOSTTY=0 ;;
    -h|--help)
      cat <<'HELP'
Usage: bash setup-ghostty-catppuccin.sh [--dry-run] [--no-open]

Options:
  --dry-run   只打印将要执行的动作，不修改系统
  --no-open   配置完成后不自动打开 Ghostty
HELP
      exit 0
      ;;
    *) echo "Unknown option: $arg" >&2; exit 1 ;;
  esac
done

log()  { printf '\033[1;32m[INFO]\033[0m %s\n' "$*"; }
warn() { printf '\033[1;33m[WARN]\033[0m %s\n' "$*"; }
err()  { printf '\033[1;31m[ERR ]\033[0m %s\n' "$*" >&2; }

run() {
  if [[ "$DRY_RUN" == "1" ]]; then
    printf '+ %q ' "$@"; printf '\n'
  else
    "$@"
  fi
}

write_file() {
  local file="$1"
  local content="$2"
  if [[ "$DRY_RUN" == "1" ]]; then
    log "Would write: $file"
  else
    printf '%s' "$content" > "$file"
  fi
}

backup_file() {
  local file="$1"
  if [[ -f "$file" ]]; then
    local backup="${file}.bak.${TS}"
    log "Backup $file -> $backup"
    run cp "$file" "$backup"
  fi
}

ensure_writable_file() {
  local file="$1"
  if [[ -e "$file" && ! -w "$file" ]]; then
    warn "$file 当前不可写，尝试修复 owner 和写权限"
    run sudo chown "$USER":staff "$file"
    run chmod u+rw "$file"
  fi
}

brew_install() {
  local pkg="$1"
  if brew list "$pkg" >/dev/null 2>&1; then
    log "Homebrew formula already installed: $pkg"
  else
    log "Install Homebrew formula: $pkg"
    run brew install "$pkg"
  fi
}

brew_cask_install() {
  local cask="$1"
  if brew list --cask "$cask" >/dev/null 2>&1; then
    log "Homebrew cask already installed: $cask"
  else
    log "Install Homebrew cask: $cask"
    run brew install --cask "$cask"
  fi
}

if [[ "$(uname -s)" != "Darwin" ]]; then
  err "当前脚本面向 macOS。Linux/WSL 可参考 README 单独适配。"
  exit 1
fi

log "Start Ghostty + Starship + Catppuccin Mocha setup"

# 1. 安装 Homebrew
if ! command -v brew >/dev/null 2>&1; then
  warn "Homebrew not found, installing Homebrew..."
  if [[ "$DRY_RUN" == "1" ]]; then
    echo '+ /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"'
  else
    /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"
  fi
fi

# 让当前 shell 立刻识别 brew
if [[ -x /opt/homebrew/bin/brew ]]; then
  eval "$(/opt/homebrew/bin/brew shellenv)"
elif [[ -x /usr/local/bin/brew ]]; then
  eval "$(/usr/local/bin/brew shellenv)"
fi

if ! command -v brew >/dev/null 2>&1; then
  err "Homebrew 安装后仍不可用，请检查 PATH。"
  exit 1
fi

log "Update Homebrew metadata"
run brew update

# 2. 安装 Ghostty、字体、Starship 和常用 CLI
brew_cask_install ghostty
brew_cask_install font-meslo-lg-nerd-font

for pkg in \
  starship \
  zsh-autosuggestions \
  zsh-syntax-highlighting \
  zsh-completions \
  eza \
  bat \
  fd \
  ripgrep \
  fzf \
  btop \
  zoxide \
  jq \
  tldr \
  git-delta \
  lazygit \
  fnm
  do
    brew_install "$pkg"
  done

# 3. 写入 Ghostty 配置
GHOSTTY_DIR="$HOME/.config/ghostty"
GHOSTTY_CONFIG="$GHOSTTY_DIR/config.ghostty"
run mkdir -p "$GHOSTTY_DIR"
backup_file "$GHOSTTY_CONFIG"

GHOSTTY_CONTENT=$(cat <<'GHOSTTY_EOF'
